void main() {
  print("Exemplo de condicionais - IF ELSE");
  int idade = 18;

  // < significa menor que
  // > significa maior que
  // == significa igual a
  // <= significa menor igual
  // >= significa menor igual

  if (idade >= 18) {
    print("Você é maior de idade.");
  } else {
    print("Você não é maior de idade.");
  }
}